def count_from_reamur(reamur):
    reamur_to_celsius = (5/4) * reamur
    reamur_to_fahrenheit = (9/4) * reamur + 32
    reamur_to_kelvin = (5/4) * reamur + 273.15
    print("Celcius : ", reamur_to_celsius)
    print("Fahrenheit : ", reamur_to_fahrenheit)
    print("Kelvin : ", reamur_to_kelvin)
    print("\n")