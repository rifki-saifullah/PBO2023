def count_from_celsius(celsius):
    celsius_to_fahrenheit = (celsius * 9/5) + 32
    celsius_to_reamur = celsius * 4/5
    celsius_to_kelvin = celsius + 273.15
    print("Fahrenheit : ", celsius_to_fahrenheit)
    print("Reamur : ", celsius_to_reamur)
    print("Kelvin : ", celsius_to_kelvin)
    print("\n")